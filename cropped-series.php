					<h5>the shoot</h5>
					<p>
						The shooting of these images was very simple and straightforward. I wanted to challenge myself with the retouching portion of the project. Therefore I created a very simple lighting setup using an iPhone and the overhead room light. I also used a very simple backdrop for my subjects. This allowed me to get the photos shot in a short amount of time, leaving lots of time for post processing.
					</p>
				</div>

