				<div class="writing">
					<h2>Creation</h2>
					<h5>the edit</h5>
					<p>
						Each photo was processed using the same techniques and workflow to keep a consistent look throughout the series. I started out by editing the facial features of each photo to really exaggerate certain features and turn them into their own unique characters. From there I cut out each head from the background and set each one to black to eliminate the backdrops. I really wanted to exaggerate the facial features and add more realism to each shot, so I used dodge and burn techniques to bring out different areas of the face. Then I added a faux shadow around the subject to make them look as though they are in a dark room with lighting just directed to the face. To finish up the images, I added a border, some sharpening, and a subtle grain to tie it all together.
					</p>
