					<h2>Conception</h2>
					<h5>the idea</h5>
					<p>
						Over the summer of 2015, my brothers became very involved in watching the HBO series Game of Thrones. We were at our family cabin for the summer and I was dying to work on some sort of project. One of my brothers had left one of the show's box lying around, and in the box were some very interesting artwork of each of the main characters. After going through and analyzing the artwork, I knew I had found my next project!
					</p>
					<h5>the shoot</h5>
					<p>
						Being at the cabin away from the usual amenities I was very limited for resources. I had no access to any quality lighting or backdrops for my subjects to stand in front of. Therefore I set out to create a makeshift studio to shoot the photos in. I set my subjects on a plastic lawn chair in a dark shed. I lit my subjects using an iPhone flashlight on the left of the face, and then a lighter on the right side to get some difference in colour tone. It certainly wasn't a high budget shoot, but that's what made it unique and enjoyable.
					</p>
				</div>

				<img src="assets/images/GOT-Originals.jpg"></img>

				<div class="writing">
					<h2>Creation</h2>
					<h5>the edit</h5>
					<p>
						Each photo was processed using the same techniques and workflow to keep a consistent look throughout the series. I started out by turning the images black and white to eliminate some unwanted colours from my makeshift light setup. I really wanted to exaggerate the facial features and add more realism to each shot, so I used dodge and burn techniques to bring out different areas of the face. Then I added back some of the colour to the images and adjusted some colour to really get the perfect look. To finish up the images, I painted out the background and added in some faux shadows to create a dark and epic look that the box art had.
					</p>
				</div>

