					<h5>the summary</h5>
					<p>
						My project is a full alphabetic series of photographs using my interpretation of different words. It consists of 30 designed pages that include a word, a photograph, and finally an explanation of why I chose to shoot that particular photograph. Through this course of study, I wanted to be able to justify why I took each photograph, and be able to explain my thought process behind each photograph. I believed that in doing this, I would start to give more thought to the photographs that I am taking. All in all it was a very successful project, I accomplished exactly what I wanted, and learned a lot.
					</p>
					<h5>the process</h5>
					<p>
						My process through the project was pretty simple once I found my groove and developed a workflow. To start I wrote out each letter of the alphabet from A to Z. With the help of a dictionary, I chose a word and definition to represent each of these letters that I would use to shoot my photographs. When I completed all of the words, I took the first 13 and printed them out to put into my camera bag. I took these 13 words and my camera, and began to read off a few of the words, all while thinking about what I could shoot for each of them. I interpreted each word based off of the definitions I chose, some I interpreted and shot literally, some I went a different route on. When I completed the shooting of the first 13 photos, I imported them and organized them. When that was completed I sat down and wrote about how I interpreted each word and why I shot the respective photographs. The second half of the photos went the exact same way.
					</p>

					<p>
						When all of the shooting and writing was completed, I started developing a consistent look for all of the photos in the series. I thought a long time about how I wanted the colours in the photos to be. After a lot of testing and thinking, I decided on showcasing the warm colours, and then contrasting that with the cool blues. I created a custom preset in Lightroom that allowed me to easily apply the colours to each photo.
					</p>

					<p>
						Finally I developed the layout. I decided on creating more of a layered look to my design, I didnâ€™t want everything to be uniform. I started with the layout of the photos, and then moved onto a simple paragraph. Once I was happy with that, I started adding various design elements to really make the pages more interesting. For the overall look, I wanted to keep simple black and white colours to make the photos pop off the page.
					</p>
