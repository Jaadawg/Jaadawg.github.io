					<h5>version one</h5>
					<p>
						This version of the Vibrant Pixels logo was created off of an initial idea of using uniform angles and lines. However, I didn't feel as though this logo really captured the vibe and feeling I wanted Vibrant Pixels to portray to its clients.
					</p>

					<img src="assets/images/Version-One.jpg"></img>

					<h5>the mark</h5>
					<p>
						The Vibrant Pixels symbol is the visual encapsulation of our brand. The symbol was developed around the visual of a television pixel. The pixel is made up of three different coloured lines which were used in the symbol in one solid colour. The three lines of the symbol represent three important aspects to our brand, creativity, client satisfaction, and quality deliverables. The three lines of the symbol are arranged in such a way to create the form of a “V” and at an angle to keep horizontal consistency with the word mark. The final aspect of the symbol is the dot, this dot is there to represent us as a brand, responsible for the three aspects surrounding us.
					</p>

					<img src="assets/images/Vibrant-Mark.jpg"></img>

					<h5>stacked mark</h5>
					<p>
						The stacked version of the logo takes a different approach to the actual name. I decided that the stacked version of the logo needed more of the brand colours. When all black, the text didn't pop as much as much as I felt it should. When all black, "Vibrant Pixels" seemed to blend together and reduced the readability. So I solved this problem by bringing in the Vibrant Pixels red into the word mark.
					</p>

					<img src="assets/images/Stacked-Mark.jpg"></img>

					<h5>alternate mark</h5>
					<p>
						The alternate logo was created to create more versatility in the brand. I wanted to create an alternate version of the logo that could work for many other medias. This rounded box surrounding the logo can work as a favicon for the website, an icon for a mobile app, with many other possibilities.
					</p>

